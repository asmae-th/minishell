#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	result;
    int     sign;

	i = 0;
	result = 0;
    sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if(str[i] == '-') 
			sign *= -1;   
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - 48);
		i++;
	}
	if (str[i] != '\0' || result > LONG_MAX || result < LONG_MIN)
		return (0);
	return (sign * result);
}
